export default function Home() {
  const [selectedChapter, setSelectedChapter] = useState(chapters[0]);
  const [questionIndex, setQuestionIndex] = useState(0);
  const [currentQuestion, setCurrentQuestion] = useState(selectedChapter.questions[questionIndex]);
  const [studentAnswer, setStudentAnswer] = useState('');
  const [showHint, setShowHint] = useState(false);
  const [hint, setHint] = useState('');
  const {toast} = useToast();

  const getQuestionsForChapter = (chapter: any) => {
    return chapter.questions;
  };

  const validateAnswer = () => {
    if (studentAnswer === currentQuestion.correctAnswer) {
      toast({
        title: 'Correct!',
        description: 'Great job! Your answer is correct.',
      });
    } else {
      toast({
        title: 'Incorrect',
        description: `Keep trying! Your answer is not correct. The correct answer is ${currentQuestion.correctAnswer}.`,
      });
    }
  };

  const handleHintRequest = async () => {
    try {
      const hintResponse = await generateHint({
        question: currentQuestion.text,
        category: currentQuestion.category,
      });
      setHint(hintResponse.hint);
      setShowHint(true);
    } catch (error) {
      console.error('Error generating hint:', error);
      toast({
        title: 'Error',
        description: 'Failed to generate hint. Please try again.',
        variant: 'destructive',
      });
    }
  };

  const goToNextQuestion = () => {
    const questions = getQuestionsForChapter(selectedChapter);
    if (questionIndex < selectedChapter.questions.length - 1) {
      setQuestionIndex(questionIndex + 1);
      setCurrentQuestion(selectedChapter.questions[questionIndex + 1]);
      setStudentAnswer('');
      setShowHint(false);
    } else {
      toast({
        title: 'End of Chapter',
        description: 'You have reached the end of the chapter.',
      });
    }
  };

  const goToPreviousQuestion = () => {
    const questions = getQuestionsForChapter(selectedChapter);
    if (questionIndex > 0) {
      setQuestionIndex(questionIndex - 1);
      setCurrentQuestion(questions[questionIndex - 1]);
      setStudentAnswer('');
      setShowHint(false);
    } else {
      toast({
        title: 'Beginning of Chapter',
        description: 'You are at the beginning of the chapter.',
      });
    }
  };

  return (
    <SidebarProvider>
      <div className="flex h-screen">
        <Sidebar collapsible="icon">
          <SidebarHeader>
            <DropdownMenu>
              <DropdownMenuTrigger asChild>
                <Button variant="ghost" className="gap-2 w-full justify-start px-2">
                  <Avatar className="h-8 w-8">
                    <AvatarImage src="https://picsum.photos/500/500" alt="Avatar"/>
                    <AvatarFallback>ZZ</AvatarFallback>
                  </Avatar>
                  <span>Olympiad Prep</span>
                  <Icons.chevronDown className="h-4 w-4 opacity-50"/>
                </Button>
              </DropdownMenuTrigger>
              <DropdownMenuContent align="end" forceMount className="w-56">
                <DropdownMenuItem>
                  <ModeToggle/>
                </DropdownMenuItem>
              </DropdownMenuContent>
            </DropdownMenu>
          </SidebarHeader>
          <SidebarSeparator/>
          <SidebarContent>
            <SidebarGroup>
              <SidebarMenu>
                {chapters.map((chapter) => (
                  <SidebarMenuItem key={chapter.id}>
                    <SidebarMenuButton onClick={() => {
                      setSelectedChapter(chapter);
                      setQuestionIndex(0);
                      setCurrentQuestion(chapter.questions[0]);
                      setStudentAnswer('');
                      setShowHint(false);
                    }}>
                      {chapter.title}
                    </SidebarMenuButton>
                  </SidebarMenuItem>
                ))}
              </SidebarMenu>
            </SidebarGroup>
          </SidebarContent>
          <SidebarFooter>
            <SidebarTrigger>
              <Button variant="outline" size="sm" className="w-full">
                <Icons.settings className="mr-2 h-4 w-4"/>
                Settings
              </Button>
            </SidebarTrigger>
          </SidebarFooter>
        </Sidebar>

        <div className="flex-1 p-4">
          <Card className="w-full h-full flex flex-col">
            <CardHeader>
              <CardTitle>{selectedChapter.title}</CardTitle>
              <CardDescription>Test your knowledge!</CardDescription>
            </CardHeader>
            <CardContent className="flex-1">
              <div className="mb-4">
                <p className="text-lg font-semibold">Question {questionIndex + 1} / {getQuestionsForChapter(selectedChapter).length}:</p>
                <p className="text-md">{currentQuestion.text}</p>
              </div>

              {currentQuestion.type === 'multiple-choice' ? (
                <RadioGroup defaultValue={studentAnswer} className="grid gap-2" onValueChange={setStudentAnswer}>
                  {currentQuestion.options.map((option) => (
                    <div key={option} className="flex items-center space-x-2">
                      <RadioGroupItem value={option} id={`r${option}`} className="text-primary" />
                      <Label htmlFor={`r${option}`}>{option}</Label>
                    </div>
                  ))}
                </RadioGroup>
              ) : (
                <div className="mb-4">
                  <Textarea
                    placeholder="Enter your answer"
                    className="w-full"
                    value={studentAnswer}
                    onChange={(e) => setStudentAnswer(e.target.value)}
                  />
                </div>
              )}
            </CardContent>
            <CardFooter className="flex justify-between">
              <Button onClick={validateAnswer}>Submit Answer</Button>
              <Button variant="secondary" onClick={handleHintRequest}>
                Get Hint
              </Button>
            </CardFooter>
            <CardFooter className="flex justify-between">
              <Button onClick={goToPreviousQuestion} disabled={questionIndex === 0}>
                Previous
              </Button>
              <Button onClick={goToNextQuestion} disabled={questionIndex === getQuestionsForChapter(selectedChapter).length - 1}>
                Next
              </Button>
            </CardFooter>
          </Card>
        </div>

      </div>

      {showHint && (
        <div className="fixed bottom-0 left-0 w-full bg-gray-100 p-4">
          <p>Hint: {hint}</p>
          <Button onClick={() => setShowHint(false)}>Close Hint</Button>
        </div>
      )}
    </SidebarProvider>
  );
}


